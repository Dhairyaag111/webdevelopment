# Typecasting is the process of converting a variable from one datatype to another datatype.

name = "Dhairya"
age = 16
gpa = 3.50
isMarried = False


print(type(name))
print(type(age))
print(type(gpa))
print(type(isMarried))

# Datatype Converting

print(bool(name))
print(str(age))
print(int(gpa))
print(str(isMarried))

